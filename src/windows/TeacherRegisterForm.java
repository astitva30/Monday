    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String userName = jTextField1.getText();
        String password = new String(jPasswordField1.getPassword());
        String name = jTextField2.getText();
        String contactNo = jTextField3.getText();
        int batches = Integer.parseInt(jTextField4.getText());
        
        SessionFactory sf = new Configuration().configure().buildSessionFactory();
       Session session = sf.openSession();
       Transaction tx = session.beginTransaction();
       
       Teacher teacher1 = new Teacher(userName,password,name,contactNo,batches);
       
       session.save(teacher1);
       tx.commit();
       session.close();
       JOptionPane.showMessageDialog(this,"Record Created!");
    }//GEN-LAST:event_jButton1ActionPerformed

